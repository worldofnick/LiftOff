#import "AUFonts.h"

@implementation AUFonts

+ (UIFont *)interstateRegularWithSize:(CGFloat)size
{
    return [UIFont fontWithName:@"Interstate-Regular" size:size];
}

+ (UIFont *)interstateLightWithSize:(CGFloat)size
{
    return [UIFont fontWithName:@"Interstate-Light" size:size];
}

@end
